	<section class="story js-anchor">
		<div class="container">
			<h2 class="header">Our Story</h2>
			<p class="body-text">What do today’s digital marketing challenges have in common with the WWII housing boom? The need for great design. Case was founded in 2004 by three design junkies and college buddies: Travis Stratford, Anthony Yumul, and Enoch Palmer. They named the agency after the Case Study House Movement, a Southern California initiative that brought architects together to design elegant and scalable home models for the booming WWII vet population. </p>
			<p class="body-text">Their shared passion for creative problem solving spurred Travis, Tony, and Enoch to rent their first studio space in SoHo, hungry to do great work and build truly collaborative partnerships with clients. If they sound like idealists, well, they are. </p>
			<p class="body-text">Twelve years in, that idealism permeates everything they do and Case continues to grow, largely organically, thanks to client referrals and long-lasting relationships with some of the world’s most iconic brands. Each founder’s unique set of strengths have contributed to Case’s success and agency culture.</p>
		</div>
